const mongoose = require('mongoose');
const { Schema } = mongoose;

const gpsSchema = new Schema({
  user_id: {
    type: String,
    required: true,
  },
  latitude: {
    type: Number,
    required: true,
  },
  longitude: {
    type: Number,
    required: true,
  },
  timestamp: {
    type: Date,
    default: Date.now,
  },
});

module.exports = mongoose.model('GPS', gpsSchema);